                    <h1>Privacyverklaring BCO Portaal</h1>

                    <h2>Over deze privacyverklaring</h2>
                    <p>
                        Deze privacyverklaring is bedoeld voor de gebruikers van het BCO-portaal. Dit is de digitale werkomgeving voor het uitvoeren van bron- en contactonderzoek naar het coronavirus. In het portaal leggen de medewerkers informatie vast uit dit onderzoek. Deze medewerkers kunnen werkzaam zijn voor de regionale GGD’en of voor de voor bron- en contactonderzoek ingezette externe partijen, zoals de landelijke partners.
                    </p>
                    <p>
                        Op de gegevensverwerking in het BCO-portaal is de Algemene verordening gegevensbescherming (afgekort: AVG) van toepassing. In deze privacyverklaring staat op welke manier we in het BCO-portaal persoonsgegevens van gebruikers verwerken en op welke manier het gebruik van de gegevens in het BCO-portaal voldoet aan de eisen van de AVG.
                    </p>
                    <p>
                        Als het nodig is passen we deze privacyverklaring aan. Dat doen we bijvoorbeeld als we iets in het portaal veranderen dat te maken heeft met de bescherming van de persoonlijke gegevens van de gebruikers van het portaal. De privacyverklaring staat op de profielpagina van de gebruiker.
                    </p>
                    <p class="last-updated">Laatste update: 29 april 2022</p>

                    <h2>Over het BCO Portaal</h2>
                    <p>
                        Het BCO-portaal is de digitale werkomgeving voor bron- en contactonderzoek van de GGD. Het BCO-portaal vormt samen met de GGD Contact-app de technische basis voor de uitvoering van het bron- en contactonderzoek naar het coronavirus. In het BCO-portaal worden persoonsgegevens verwerkt van mensen die positief zijn getest op corona en van mensen waarmee zij in een bepaalde periode in contact zijn geweest. Deze gegevens worden verzameld tijdens het telefoongesprek met de medewerker en/of met de GGD  gedeeld via de GGD Contact-app.
                    </p>

                    <h2>Logging en monitoring</h2>
                    <p>
                        In het BCO-portaal wordt informatie verwerkt van mensen die positief zijn getest op corona en van mensen waarmee zij in een bepaalde periode in contact zijn geweest. Deze informatie wordt tijdens het bron- en contactonderzoek door medewerkers geïnventariseerd. De informatie die wordt verwerkt is privacygevoelig, omdat het gezondheidsgegevens van mensen zijn.
                    </p>
                    <p>
                        We vinden het belangrijk om aan te tonen dat de GGD’en zorgvuldig en veilig met dit soort gegevens omgaan. We willen daarom voldoen aan de eisen van de NEN-normeringen voor informatiebeveiliging in de zorg (NEN 7510, NEN 7512 en NEN 7513). Zo kunnen we persoonlijke gegevens nog beter beschermen. Logging en monitoring zijn in dit kader verplicht.
                    </p>

                    <h2>Verwerkte (persoons)gegevens van gebruikers</h2>
                    <p>
                        In het BCO-portaal registeren we automatisch bepaalde gegevens en activiteiten van de medewerkers bron- en contactonderzoek. We registeren wie een dossier heeft ingezien of gewijzigd en op welk tijdstip. Wordt er voor het opvragen of verifiëren van gegevens gebruik gemaakt van de SBV-Z koppeling, dan wordt ook dat vastgelegd. Hetzelfde geldt voor het verzenden van berichten aan de index of een contact via MijnGGDContact. Verder loggen we wie op welk tijdstip in het systeem heeft ingelogd en als het mogelijk is ook de locatie en het unieke nummer van de apparatuur die voor het inloggen werd gebruikt. Het doel is om privacygevoelige gegevens te beschermen.
                    </p>

                    <h2>Verantwoordelijkheid</h2>
                    <p>
                        Het BCO-portaal is ontwikkeld door het ministerie van Volksgezondheid, Welzijn en Sport (afgekort: VWS) in samenwerking met de GGD’en, vertegenwoordigd door GGD GHOR Nederland. De minister van VWS is verwerkingsverantwoordelijk voor persoonsgegevens die worden verwerkt tijdens de ontwikkeling en het testen van het BCO-portaal. Vanaf het moment dat het portaal in gebruik wordt genomen zijn de GGD’en van de betreffende regio's verwerkingsverantwoordelijk.
                    </p>

                    <h2>Doel</h2>
                    <p>
                        Het doel van het BCO-portaal is om het bron- en contactonderzoek makkelijker, beter en sneller te kunnen uitvoeren. Bron- en contactonderzoek helpt om verspreiding van het coronavirus te beperken, en dit is een wettelijke taak van de GGD’en.
                    </p>

                    <h2>Grondslag</h2>
                    <p>
                        GGD’en hebben een gerechtvaardigd belang om passende technische en organisatorische maatregelen te nemen bij het verwerken van persoonsgegevens.
                    </p>
                    <p>
                        Hiervoor gelden:
                    </p>
                    <ul class="list">
                        <li>AVG</li>
                        <li>Wet publieke gezondheid</li>
                        <li>Van toepassing zijnde NEN-normeringen</li>
                    </ul>

                    <h2>Doorgifte van gegevens aan derde partijen</h2>
                    <p>
                        De volgende externe partijen ontvangen gegevens uit het BCO-portaal:
                    </p>
                    <ul class="list">
                        <li>Intermax: hostingpartij. Intermax ontvangt persoonsgegevens om het BCO-portaal te kunnen hosten en is daarom verwerker.</li>
                        <li>Het CIBG: als beheerder van de Sectorale Berichten Voorziening in de Zorg (SBV-Z). Dit is een technische koppeling die door BCO-portaal gebruikt wordt om de gegevens die van indexen en contacten zijn ingevoerd te verifiëren.</li>
                        <li>Ingeval gebruik wordt gemaakt van werkverdeling: de hiervoor ingeschakelde andere regionale GGD’en of landelijke partners.</li>
                    </ul>
                    <p>
                        Gegevens met betrekking tot logging worden doorgegeven aan:
                    </p>
                    <ul class="list">
                        <li>RIVM: voor evaluatie van de bijdrage van het BCO-portaal aan het bron- en contactonderzoek (alleen anonieme gegevens m.b.t. het gebruik).</li>
                        <li>GGD GHOR: draagt zorg voor de monitoring van functioneel gebruik van de applicatie.</li>
                        <li>Ministerie van VWS: draagt zorg voor het uitvoeren van logging en monitoring van de applicatie en infrastructuur. Daarbij maakt VWS gebruik van de inzet van Intermax en NFIR.</li>
                    </ul>
                    <p>
                        We geven geen persoonsgegevens door aan landen buiten de Europese Unie of Europese Economische Ruimte, ook niet via onze partners.
                    </p>

                    <h2>Bewaartermijnen gegevens logging</h2>
                    <p>
                        De loggegevens worden bewaard tot maximaal vijf jaar na de datum waarop deze zijn aangemaakt. De gegevens worden na afloop van deze termijn verwijderd.
                    </p>

                    <h2>Rechten van betrokkenen</h2>
                    <p>
                        Gebruikers van GGD Contact die vragen hebben over de verwerking van hun persoonsgegevens of de logging en monitoring van het systeem kunnen deze stellen aan de Functionaris Gegevensbescherming van de GGD waarvoor ze werken. Zie hiervoor de website van de betreffende GGD.
                    </p>
                    <p>
                        Gebruikers kunnen klachten over de verwerking van persoonsgegevens melden bij de Autoriteit Persoonsgegevens. Kijk voor meer informatie hierover op de website van de Autoriteit Persoonsgegevens.
                    </p>
                    <p>
                        Voor vragen over de verwerking van persoonsgegevens tijdens de ontwikkeling van het BCO-portaal kunnen gebruikers contact opnemen met de Functionaris Gegevensbescherming van het ministerie van Volksgezondheid, Welzijn en Sport. Meer informatie hierover staat op <a href="https://www.rijksoverheid.nl/ministeries/ministerie-van-volksgezondheid-welzijn-en-sport/avg/contact/functionaris-gegevensbescherming" target="_blank" rel="noopener noreferrer">de website van het ministerie van VWS</a>.
                    </p>

                    <h2>Beveiliging</h2>
                    <p>
                        De minister van VWS, GGD GHOR Nederland en de GGD’en nemen de bescherming van uw gegevens zeer serieus. We nemen passende maatregelen om misbruik, verlies, onbevoegde toegang, ongewenste openbaarmaking en ongeoorloofde wijziging tegen te gaan. De vastlegging van gegevens van gebruikers is daar onderdeel van. Alleen mensen voor wie het voor het uitvoeren van hun functie noodzakelijk is mogen de logging- en monitoringsgegevens bekijken.
                    </p>
